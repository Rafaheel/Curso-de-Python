# Escreva um programa que leia um valo em metros e o exiba convertido em centimetros e milimetros


m2 = int(input("Informe a metragem: "))
print(
    f"A metragem escolhida foi de: {m2}, onde seu valor em centimetros é de {m2 * 100 }cm")
print(
    f"A metragem escolhida foi de: {m2}, onde seu valor em milimetros é de {m2 * 1000}mm")
