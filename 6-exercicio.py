print(f"O valor escolhido foi {n}, e seu dobro é de {n * 2}")
print(f"O valor escolhido foi {n}, e seu triplo é de {n * 3}")
print(
    f"O valor escolhido foi {n}, e sua triplo raiz quadrada é de {n ** (1/2)}")
